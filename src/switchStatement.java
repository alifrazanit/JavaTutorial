public class switchStatement {
    public static void main(String[] args) {
        var nilai = 'A';

        switch (nilai){
            case 'A':
                System.out.println("Lulus dengan nilai A");
                break;
            case 'B':
                System.out.println("Lulus dengan nilai B");
                break;
            default:
                System.out.println("Lulus dengan nilai C");
                break;
        }
    }
}
