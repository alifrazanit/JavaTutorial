public class OperasiMatematika {
    public static void main(String[] args) {
        int a = 2;
        int b = 9;

        int penjumlahan = a + b;
        int pengurangan = a - b;
        int perkalian = a * b;
        int pembagian = a / b;
        int modulusSisaBagi = a % b;

        int d = 10;

        d *= 2;

        d++;
        d--;



        System.out.println("Penjumlahan : " + penjumlahan);
        System.out.println("pengurangan : " + pengurangan);
        System.out.println("perkalian : " + perkalian);
        System.out.println("pembagian : " + pembagian);
        System.out.println("modulusSisaBagi : " + modulusSisaBagi);
    }
}
